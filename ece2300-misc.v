`ifndef ECE2300_MISC_V
`define ECE2300_MISC_V

//------------------------------------------------------------------------
// ECE2300_UNUSED
//------------------------------------------------------------------------

`define ECE2300_UNUSED( signal_ )                                       \
  logic [$bits(signal_)-1:0] \signal_``_unused ;                        \
  assign \signal_``_unused = signal_;                                   \
  if (1)

//------------------------------------------------------------------------
// ECE2300_UNDRIVEN
//------------------------------------------------------------------------

`define ECE2300_UNDRIVEN( signal_ )                                     \
  assign signal_ = 'z;                                                  \
  if (1)

`endif /* ECE2300_MISC */

